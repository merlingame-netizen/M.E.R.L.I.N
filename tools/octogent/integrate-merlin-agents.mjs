#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────────────
// integrate-merlin-agents.mjs
//
// Pre-populates Octogent's `.octogent/tentacles/` directory with one
// tentacle per MERLIN studio agent (103 agents from
// tools/autodev/agent_cards/_registry.json).
//
// Each tentacle becomes:
//   .octogent/tentacles/<id>/CONTEXT.md   (H1 + description + capabilities)
// And the deck state gets:
//   .octogent/state/deck.json            (color + tags + scope per tentacle)
//
// Idempotent: re-running updates content but doesn't lose user-added
// vault notes or todos.
//
// Usage (from any cwd inside the MERLIN repo, run via WSL bash):
//   node tools/octogent/integrate-merlin-agents.mjs
//
// Schema docs:
//   tools/octogent/apps/api/src/deck/readDeckTentacles.ts
// ─────────────────────────────────────────────────────────────────────────

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OCTOGENT_DIR = __dirname;                       // tools/octogent/
const MERLIN_ROOT = resolve(OCTOGENT_DIR, "..", ".."); // repo root
const CARDS_DIR = join(MERLIN_ROOT, "tools", "autodev", "agent_cards");
const REGISTRY = join(CARDS_DIR, "_registry.json");
const TENTACLES_DIR = join(OCTOGENT_DIR, ".octogent", "tentacles");
const STATE_DIR = join(OCTOGENT_DIR, ".octogent", "state");
const PROJECT_JSON = join(OCTOGENT_DIR, ".octogent", "project.json");
const DECK_STATE_PATH = join(STATE_DIR, "deck.json");

// Druido-Tech palette mapping MERLIN agent categories → tentacle color.
// Matches the project's design language (see merlin_visual.gd / CRT theme).
const CATEGORY_COLOR = {
  core:          "#4A6FA5", // slate blue — engine/runtime
  creative:      "#B964D8", // violet — magic / shader / asset
  knowledge:     "#E6BD52", // ogham gold — lore / docs
  llm:           "#2EE6D6", // phosphor cyan — LLM / AI
  narrative:     "#C58A4F", // parchment amber — story / cards
  ops:           "#84A66E", // forest green — git / deploy / pipeline
  orchestration: "#E8742C", // orange — agents-of-agents
  quality:       "#C8503A", // crimson — review / test / audit
  "ui-ux":       "#8B6BC8", // heliotrope — interface / UX
  misc:          "#7A8794", // grey fallback
};

const isStubDescription = (s) =>
  !s || /^auto-generated by create_agent\.py/i.test(String(s).trim());

const ensureDir = (p) => { if (!existsSync(p)) mkdirSync(p, { recursive: true }); };

// ── Read registry ─────────────────────────────────────────────────────────
if (!existsSync(REGISTRY)) {
  console.error(`[integrate] Registry missing: ${REGISTRY}`);
  process.exit(1);
}
const registryRaw = JSON.parse(readFileSync(REGISTRY, "utf-8"));
const registry = Array.isArray(registryRaw) ? registryRaw : (registryRaw.agents ?? []);

console.log(`[integrate] Registry: ${registry.length} agents`);
ensureDir(TENTACLES_DIR);
ensureDir(STATE_DIR);

// ── Build deck state + tentacle contexts ──────────────────────────────────
const deckState = { tentacles: {} };
const writtenContexts = [];
const skippedExisting = [];

for (const entry of registry) {
  const id = String(entry.id ?? "").trim();
  if (!id) continue;
  const name = String(entry.name ?? id).trim();
  const category = String(entry.category ?? "misc").trim().toLowerCase();
  const color = CATEGORY_COLOR[category] ?? CATEGORY_COLOR.misc;

  // Pull richer description from per-agent card if present.
  let description = String(entry.description ?? "").trim();
  let extendedCard = null;
  const cardPath = join(CARDS_DIR, `${id}.json`);
  if (existsSync(cardPath)) {
    try {
      extendedCard = JSON.parse(readFileSync(cardPath, "utf-8"));
      const cardDesc = String(extendedCard.description ?? "").trim();
      if (!isStubDescription(cardDesc)) description = cardDesc;
    } catch {
      // ignore card parse errors — fall back to registry description
    }
  }

  // If description still stubby, synthesize from capabilities.
  if (isStubDescription(description)) {
    const caps = Array.isArray(entry.capabilities)
      ? entry.capabilities.filter((c) => typeof c === "string")
      : [];
    description = caps.length > 0
      ? `MERLIN ${category} agent. Capabilities: ${caps.join(", ")}.`
      : `MERLIN ${category} agent.`;
  }

  // Build CONTEXT.md content.
  const lines = [
    `# ${name}`,
    "",
    description,
    "",
    "## Source",
    "",
    `- **Category**: ${category}`,
    `- **Cost profile**: ${entry.cost_profile ?? "sonnet"}`,
    `- **Registry entry**: \`tools/autodev/agent_cards/_registry.json#${id}\``,
  ];
  if (extendedCard?.agent_file) {
    lines.push(`- **Definition**: \`${extendedCard.agent_file}\``);
  }
  // Include capabilities (array of strings OR objects with task_type)
  const caps = Array.isArray(extendedCard?.capabilities) ? extendedCard.capabilities : entry.capabilities;
  if (Array.isArray(caps) && caps.length > 0) {
    lines.push("", "## Capabilities", "");
    for (const c of caps) {
      if (typeof c === "string") {
        lines.push(`- ${c}`);
      } else if (c && typeof c === "object" && "task_type" in c) {
        const kw = Array.isArray(c.keywords) ? c.keywords.join(", ") : "";
        const conf = typeof c.confidence === "number" ? ` _(conf ${c.confidence})_` : "";
        lines.push(`- **${c.task_type}**${conf}: ${kw}`);
      }
    }
  }
  // Inputs / outputs (only present in extendedCard). Each entry is an
  // object {name, type, required?, description}. Render as:
  //   - **name** _(type, required?)_: description
  const renderIO = (item) => {
    if (typeof item === "string") return `- ${item}`;
    if (item && typeof item === "object") {
      const n = String(item.name ?? "");
      const t = String(item.type ?? "");
      const req = item.required ? ", required" : "";
      const desc = String(item.description ?? "");
      const meta = (t || req) ? ` _(${t}${req})_` : "";
      return `- **${n}**${meta}${desc ? `: ${desc}` : ""}`;
    }
    return `- ${String(item)}`;
  };
  if (extendedCard) {
    if (Array.isArray(extendedCard.inputs) && extendedCard.inputs.length) {
      lines.push("", "## Inputs", "", ...extendedCard.inputs.map(renderIO));
    }
    if (Array.isArray(extendedCard.outputs) && extendedCard.outputs.length) {
      lines.push("", "## Outputs", "", ...extendedCard.outputs.map(renderIO));
    }
  }
  lines.push("", "---", "", "_Generated by `tools/octogent/integrate-merlin-agents.mjs`. Edit freely — re-running this script preserves user-added files in the tentacle folder._", "");

  const tentacleDir = join(TENTACLES_DIR, id);
  ensureDir(tentacleDir);
  const contextPath = join(tentacleDir, "CONTEXT.md");

  // Idempotency: only overwrite the auto-generated portion. We tag it with
  // a sentinel so user edits below the fold are preserved on rerun.
  const SENTINEL = "<!-- OCTOGENT_AUTOGEN -->";
  const SENTINEL_END = "<!-- /OCTOGENT_AUTOGEN -->";
  const generated = `${SENTINEL}\n${lines.join("\n")}\n${SENTINEL_END}\n`;

  let finalContent;
  if (existsSync(contextPath)) {
    const existing = readFileSync(contextPath, "utf-8");
    if (existing.includes(SENTINEL) && existing.includes(SENTINEL_END)) {
      // Replace only between sentinels.
      const before = existing.split(SENTINEL)[0];
      const after = existing.split(SENTINEL_END)[1] ?? "";
      finalContent = before + generated + after;
    } else {
      // No sentinels — user-authored; prepend our block, keep theirs.
      finalContent = generated + "\n" + existing;
      skippedExisting.push(id);
    }
  } else {
    finalContent = generated;
  }
  writeFileSync(contextPath, finalContent);
  writtenContexts.push(id);

  // Deck state entry — color + scope tags.
  deckState.tentacles[id] = {
    color,
    status: "idle",
    octopus: { animation: null, expression: null, accessory: null, hairColor: null },
    scope: {
      paths: [],
      tags: [category, `cost:${entry.cost_profile ?? "sonnet"}`],
    },
  };
}

// ── Write deck state ──────────────────────────────────────────────────────
writeFileSync(DECK_STATE_PATH, `${JSON.stringify(deckState, null, 2)}\n`);

// ── Project displayName patch (was "octogent" from first cd-into-tools launch) ──
if (existsSync(PROJECT_JSON)) {
  try {
    const proj = JSON.parse(readFileSync(PROJECT_JSON, "utf-8"));
    if (proj.displayName !== "MERLIN") {
      proj.displayName = "MERLIN";
      writeFileSync(PROJECT_JSON, `${JSON.stringify(proj, null, 2)}\n`);
      console.log(`[integrate] project.json displayName -> MERLIN`);
    }
  } catch (e) {
    console.warn(`[integrate] project.json patch failed: ${e.message}`);
  }
}

// ── Summary ───────────────────────────────────────────────────────────────
console.log(`[integrate] tentacles written: ${writtenContexts.length}`);
console.log(`[integrate] deck state: ${DECK_STATE_PATH}`);
if (skippedExisting.length) {
  console.log(`[integrate] preserved user content (${skippedExisting.length}): ${skippedExisting.slice(0, 5).join(", ")}${skippedExisting.length > 5 ? "..." : ""}`);
}
console.log(`[integrate] done.`);
